# Decimal number representation
decimal_num = 10

# Binary number representation
binary_num = 0b1010  # Equals 10 in decimal
print(f'Binary representation of {decimal_num} is {binary_num}')

# Octal number representation
octal_num = 0o12  # Equals 10 in decimal
print(f'Octal representation of {decimal_num} is {octal_num}')

# Hexadecimal number representation
hexadecimal_num = 0xA  # Equals 10 in decimal
print(f'Hexadecimal representation of {decimal_num} is {hexadecimal_num}')



